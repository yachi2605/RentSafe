def normalize_text(value: str | None) -> str:
    """Collapse whitespace and trim a required string-like value."""
    return " ".join((value or "").split()).strip()


def normalize_optional_text(value: str | None) -> str | None:
    cleaned = normalize_text(value)
    return cleaned or None


def normalize_city(value: str | None) -> str:
    cleaned = normalize_text(value)
    return cleaned.title()


def normalize_state(value: str | None) -> str:
    cleaned = normalize_text(value)
    if not cleaned:
        return ""

    abbreviation = cleaned.upper()
    if abbreviation in STATE_CODE_TO_NAME:
        return STATE_CODE_TO_NAME[abbreviation]

    titled = cleaned.title()
    return titled if titled in US_STATES else cleaned
